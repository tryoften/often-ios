        view.backgroundColor = UIColor.oftDarkGrey74Color()

        if let packVC = self.packsVC {
            packVC.view.translatesAutoresizingMaskIntoConstraints = false
            self.view.addSubview(packVC.view)
            self.view.addConstraints([
                packVC.view.al_top == self.view.al_top,
                packVC.view.al_bottom == self.view.al_bottom,
                packVC.view.al_left == self.view.al_left,
                packVC.view.al_right == self.view.al_right
            ])
        }
